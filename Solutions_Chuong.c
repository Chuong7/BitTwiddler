/* 
 * bitMask - Generate a mask consisting of all 1's 
 *   lowbit and highbit
 *   Examples: bitMask(5,3) = 0x38
 *   Assume 0 <= lowbit <= 31, and 0 <= highbit <= 31
 *   If lowbit > highbit, then mask should be all 0's
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
 
/* did the high < low scenario last. larger low value would result in 0 after double 
shifts. mistakes resulted in trying to get highbit shift to position. forgot that 
flipping the bits needed to be reflected in one place over when filling with 1s
*/ 
int bitMask(int highbit, int lowbit) {

  int a = ~0;
  int b = a << highbit;
  int c = b << 1;
  int d = ~c;
  int e = a << lowbit;
  int f = e & d;
  int g = 1;
  int h = g << highbit;
  int i = h >> lowbit;
  int j = !!i;
  int k = ~j+1;
  return f & k;
  
}

/* hardest method in this lab. since corner cases were hard to 
solve and mapping of outside parameters to binary representation 
provided unexpected results. must be careful of 15 vs 16 >> shift. shift 
single one out to create 1 and all 0s or invert it for opposite effect
*/
int fitsShort(int x) {


	int a = ~0 << 15;
	int c = a & x;
	int d = c >> 15;
	int e = d + 1;
	int f = ~(a << 16);
	int g = f << 1;
	int h = g & e;
	int i = !h;
	return i;
	
}
